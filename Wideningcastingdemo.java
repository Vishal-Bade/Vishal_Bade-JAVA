

public class Wideningcastingdemo {
    public static void main(String[] args) {
        int a = 10;
        double a1=a;
        
        System.out.println(a);
        System.out.println(a1);
        
        double b=9.55;
        int b1=(int) b;
        
        System.out.println("------");
        
        System.out.println(b);
        System.out.println(b1);
        
        System.out.println(" ");
    }
}
